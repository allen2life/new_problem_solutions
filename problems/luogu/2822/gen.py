import random


def main():
    random.seed()
    t = random.randint(1, 30)
    k = random.randint(2, 21)
    print(t, k)
    for _ in range(t):
        n = random.randint(0, 80)
        m = random.randint(0, 80)
        print(n, m)


if __name__ == "__main__":
    main()
