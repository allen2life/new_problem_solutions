#!/usr/bin/env python3
import random


def main():
    random.seed()
    t = random.randint(1, 6)
    print(t)
    for _ in range(t):
        n = random.randint(1, 4)
        k = random.randint(0, n)
        print(n, k)


if __name__ == "__main__":
    main()
